function [ tf, x, y ] = validrd( x, y )
%VALIDRD Summary of this function goes here
%   Detailed explanation goes here
    rd_boundaries = [141000, 629000; ...
                     100000, 600000; ...
                      80000, 500000; ...
                      -7000, 392000; ...
                      -7000, 336000; ...
                     101000, 336000; ...
                     161000, 289000; ...
                     219000, 289000; ...
                     300000, 451000; ...
                     300000, 614000; ...
                     259000, 629000; ...
                     141000, 629000];
    tf = inpolygon(x, y, rd_boundaries(:, 1), rd_boundaries(:, 2));
    if nargout == 3 && sum(~tf) > 0
        projections = migtap.project_point_on_multiline([x(~tf)'  y(~tf)'], rd_boundaries);
        x(~tf) = projections(:, 1);
        y(~tf) = projections(:, 2);
    end
end

